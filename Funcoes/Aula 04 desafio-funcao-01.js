// Funcao: 2 params -> console.log(+)
// Funcao: 2 params -> console.log(-)

function somar(n1, n2) {
  console.log(`A soma entre ${n1} + ${n2} = ${n1 + n2}`)
}

function subtrair(n1, n2) {
  console.log(`A subtracao entre ${n1} - ${n2} = ${n1 - n2}`)
}

somar(3, 4)
somar(31, 98)

subtrair(11, 7)
subtrair(19, 52)
