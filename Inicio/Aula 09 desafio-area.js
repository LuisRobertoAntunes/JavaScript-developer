//CALCULE A AREA DA CIRCUFERENCIA
// PI * raio * raio  = area de uma circuferencia 

const PI = 3.14
const raio = 10
let areaCircuferencia = PI * raio * raio

console.log (`O valor da área é: ${areaCircuferencia}m2!`)

areaCircuferencia = Math.PI * raio * raio
